export const HARM_PARAM_CODES = HARM_PARAMS.map((p) => p.code);

export type HarmAnswers = Record<string, boolean>;

export type HarmDegree = "Высокая" | "Средняя" | "Низкая";

/**
 * If a subject's data can be harmed in different ways to different degrees,
 * the higher degree applies (this is the rule stated inline in the source
 * document, per Приказ №178) — so the summary degree is whichever is highest
 * among the answered-yes parameters, defaulting to "Низкая" if none apply.
 */
export function computeHarmDegree(answers: HarmAnswers): HarmDegree {
  const isYes = (level: HarmLevel) => HARM_PARAMS.some((p) => p.level === level && answers[p.code]);
  if (isYes("high")) return "Высокая";
  if (isYes("medium")) return "Средняя";
  return "Низкая";
}
